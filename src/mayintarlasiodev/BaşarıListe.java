    private void ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ekleActionPerformed
       OyunEkrani oyun = new OyunEkrani();
        String adi = ad.getText();
        if (adi.equals("") || adi.equalsIgnoreCase("")) // Neredeyse hazır kod parçası.
        {
           ad.setText("");
        }
        else
        {
         
          listModel.addElement(adi + "                   " + puan);
          jList1.setModel(listModel);
          ad.setText("");
          flag = true;
        }
    
    }//GEN-LAST:event_ekleActionPerformed

    
    
    private void dosya_kaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dosya_kaydetActionPerformed
        PrintWriter writer =null;   //Hazır Kod Parçası
        int boyu = jList1.getModel().getSize();
        try
        {
          writer =new PrintWriter("List.txt");
          writer.println(boyu);
          for(int i=0 ; i<boyu ; i++)
          {
             
             writer.println(" "+jList1.getModel().getElementAt(i));
          }
        }
        catch(Exception e)
        {
          System.out.println(""+e);
        }
        finally
        {
            writer.close();
        }
        
    }//GEN-LAST:event_dosya_kaydetActionPerformed

