    private void basariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_basariActionPerformed
        if(flag)  //Hazır Kod Parçası
        {
            BufferedReader br =null;
            try
            {
              br = new BufferedReader(new FileReader("List.txt"));
              int boy = Integer.parseInt(br.readLine());

              for(int i=0 ; i<boy ; i++)
              {
                  String ss = br.readLine();
                  listModel.addElement(ss);

              }
              jList1.setModel(listModel);
              flag = false;
            }
            catch(Exception e)
            {
              System.out.println(""+e);

            }
            finally
            {
                try 
                {
                    br.close();
                } 
                catch (Exception e) 
                {
                    System.out.println(""+e);
                }
            }
        }
    }//GEN-LAST:event_basariActionPerformed

